#comparing lists
my_list = [1, 2, 3, 4]
your_list = [4, 3, 2, 1]
his_list = [1, 2, 3, 4]

print(my_list == your_list)
print(my_list == his_list)

#comparing dictionaries, ORDER DOES NOT MATTER IN DICTIONARIES
my_dict = {1:1, 2:2, 3:3, 4:4}
your_dict = {4:4, 3:3, 2:2, 1:1}
print(my_dict == your_dict)