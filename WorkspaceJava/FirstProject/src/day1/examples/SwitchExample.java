package day1.examples;

public class SwitchExample {

	public static void main(String[] args) 
	{
	
		String j = "three";
		
		switch (j)
		{
		
		case "zero": System.out.println("The value is 0");
		break;
		
		case "one": System.out.println("The value is 1");
		break;
		
		case "two": System.out.println("The value is 2");
		break;
		
		case "three": System.out.println("The value is 3");
		break;
		
		default: System.out.println("No value");
		break;
		
		}
		
	}

}
